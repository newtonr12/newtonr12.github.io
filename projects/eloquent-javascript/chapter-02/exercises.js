function drawChessboard(boarder) {
 var chessboard = '';
 for (var black = 0; black < boarder; black++) {
   for (var white = 0; white < boarder; white++) {
     if ((black + white) % 2 == 0)
       chessboard += ' ';
       else
       chessboard += '#';
   }
   chessboard += '\n';
 }
 console.log(chessboard);
}

